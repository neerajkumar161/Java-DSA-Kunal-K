public class Basics {
  public static void main(String[] args) {
    if (true)
      System.out.println("This line is true!!");

    int count = 1;
    while (count < 4) {
      System.out.println(count);
      count++;
    }

    for (int i = 1; i < 4; i++) {
      System.out.println(i);
    }
  }
}
